// url is used to request data from servers - http protocol
// browser - sends request message, server - sends response message
// servers are just computers - whose job is to always make the resources available
// -much bigger than your laptop :)
// cloud - bunch of computers connected together
// request messages are what the users are sending
// poper server sends a correct response
// REQUEST message
// method, url, http version
// GET - default request method that the browser performs
// headers - meta information about our request, key-value pairs
// - info about our message
// body (optional) - not used if you get a resource
// - used when adding a data to resource (also called payload)
// RESPONSE message
// http version, status code, status text. headers
// status code - signals result of request
// e.g. 200 is request successful
// 404 - request is not found
